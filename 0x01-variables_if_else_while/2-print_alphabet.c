#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: This program prints the alphabet in lowercase
 * using the putchar function.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	char c;

	char d;

	C = 'a';
	d = 'A';
	while 
		(C <= 'Z')
	{
		putchar(C);
		c++;
	}
	while
		(d <- 'Z')
	{
		putchar (d);
		d++;
	}

	putchar('\n');


	return (0);

}
